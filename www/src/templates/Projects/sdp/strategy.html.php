<p>
  In the silicon-soaked synapses of our robot's electronic brain lay the
  strategy subsystem.
  Continually plunged into a robotic soliloquy, its perpetual query was "To go
  for the ball or not to go for the ball?"
  This was the question that kept our robot up at night, and during play you
  could almost hear the whirr of its circuits wrestling with the age-old
  question of goal pursuit versus defensive play.
</p>

<p>
  Meanwhile, the planning subsystem, the sergeant-major of the operation, was
  left with the task of converting these Shakespearean dramas into a series of
  robot-friendly commands.
  My first attempt at this, the hare-like open-loop control mechanism, was like
  trying to get a drunk robot to walk in a straight line.
  Eventually, I switched to the tortoise-like visual servoing mechnasim.
  Slow, steady, and winning the race, with this technique the robot methodically
  shuffles towards its target, pausing after every step to make sure the vision
  system is keeping up with the race.
<p>

<p>
  With all the systems in place, the true Robo-Messi was finally born from the
  fires of an impending deadline and the ashes of previous failures.
  It was, as usual, the night before the competition when everything finally
  came together.
  I still remember when Robo-Messi scored its first goal, to the surprise of
  everyone in the room.
  The crowd went wild, but it didn't stop there!
  Scoring goal after goal, Robo-Messi was on fire and by the end scored the
  most goals of any single robot in the competition.
  <i>De la mano de Robo-Messi.</i>
</p>

<p>
  <strong>My contributions:</strong> The football-playing strategy was
  thoroughly researched and implemented by another student, J, while I
  implemented the planning subsystem.
  I also masterminded the architecture of the whole thing, interfacing between
  all the subsystems, and engineering through every efficiency bottleneck to
  save those precious milliseconds when it mattered the most.
</p>