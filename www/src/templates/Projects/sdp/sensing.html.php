  <dt>Blue and green</dt>
  <dd>
    Collision sensors, in charge of making sure our Robo-Messi doesn't turn
    into a robo-bulldozer, breaking through the walls, or repeatedly smashing
    into an opponent.
    It was an accident ref!
  </dd>
  <dt>Red</dt>
  <dd>    
