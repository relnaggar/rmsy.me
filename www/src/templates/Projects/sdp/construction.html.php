<p>
  Imagine if you will, a group of Informatics students huddled over an
  assortment of LEGO pieces, brows furrowed in concentration, hands moving with
  frenzied precision.
  Their mission: to construct a mechanical wonder that could glide across the
  field, swoop onto the ball, and fire off goal-bound missiles with sniper-like
  accuracy.
</p>

<p>
  Our grand vision for this robotic superstar emerged in a whirlwind of
  creativity during an intense initial brainstorming session.
  We envisaged a swift two-wheeled sprinter with two ball bearings for
  stability, front-mounted grabbers for possessing the ball, and a unique
  spinning kicker that rotates through the center of the robot.
  However, like a toddler learning to walk, our early prototypes had a knack for
  taking a tumble.
  We'd watch in despair as our creation, eager to perform a graceful turn,
  would instead end up impersonating a beached whale.
  After several facepalm-worthy moments and robot casualties, I had an "aha!"
  moment -- a wider base and a lower centre of gravity.
  It was a game-changer, dramatically enhancing our bot's balance and poise.
</p>

<p>
  <strong>My contributions:</strong> leading the research and brainstorming
  session; constructing several experimental kicker and movement prototypes;
  collaborating with another student, H, to build the final robot from scratch.
</p>