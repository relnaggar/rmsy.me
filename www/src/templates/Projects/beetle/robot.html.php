<p>
  By the way, if all this polarisation nonesense has you scratching your head,
  here's the 101.
  Polarised light is light that oscillates in a specific direction.
  This direction is different depending on where you look in the sky, and the
  pattern across the sky changes based on the sun's position.
  Nevertheless, if you're looking straight up from a fixed ground position, the
  angle of polarisation remains stable enough for navigation.
  Like the North Star for humans, polarisation helps lost dung beetle souls find
  their path through the wilderness.
  But wait!
  Before you run outside and peer at the sky in vain, you should know that
  humans can't tell the difference between polarised and unpolarised light.
  But guess who can?
  Who's a good robot, who's a good robot?
  That's right, Pooptimus can!
</p>

<p>
  Check out the light sensors in the figure.
  A polarising filter covers the entire setup, permitting only the light
  polarised in a specific direction to pass through.
  As each sensor has its own mini-filter set at a different angle, varying
  light quantities reach each one.
  These four data points together empower Pooptimus to calculate the sky's
  polarisation angle at any moment.
  And with that, he's ready to roll!
</p>
