<p>
  But Pooptimus needs more than just movement to be a true dung beetle.
  He has to see like one, too.
  That's why he has a built-in digital camera set to gaze up into the heavens,
  with a fish-eye lens allowing him to perceive light from across a large chunk
  of the sky simultaneously.
  He's also equipped with a custom-built array of four light sensors, each
  filtered at a different angle of polarisation.
  Together, these enable him to calculate the angle of polarisation of the light
  coming from the sky, just like his dung beetle brethren.
</p>

