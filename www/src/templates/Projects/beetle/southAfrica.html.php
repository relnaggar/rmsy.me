<p>
  Oh and the robot worked!
  There was a couple of unforseen weather issues but we got some good data.
  The results showed that the robot was able to go just as straight as the
  beetles under the same skies, and that the weighting of the cues was adapting
  to the situation.
  Thus, my adapted CX model was validated, and we can be confident that it's a
  good representation of what's really going on under the beetle's bonnet.
  Brains are complicated, but one small step for science is one big leap for
  beetle-kind!
</p>

