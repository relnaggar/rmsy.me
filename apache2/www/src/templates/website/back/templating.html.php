<p>
  The exact details of what I did here are the reason Snorlax is still sleeping on Route 12, so I'll just explain the general idea of a template engine.
  You see, when we're building a website, we often want to use the same bits and pieces of code over and over again.
  For example, you might have noticed that every page on this website has the same header and footer.
  I could have copied the code for the header and footer onto each page individually, but that would be a pain in the butt.
  What if I wanted to change something later?
  I'd have to go and change it on every single page again -- no thanks!
  Even worse, this would violate the most sacred rule of programming: Don't Repeat Yourself (DRY).
  This is also known as the <strong>laziness principle</strong>, which I take very seriously, and am continuously striving to uphold.  
</p>

<p>
  That's where a "template engine" comes in.
  A template is like a cookie cutter that we can use over and over again.
  We give it the dough (our data) and the cutter (the template), and it gives us back a beautiful cookie (or in our case, a chunk of our website).
  You can use the same cutter to make lots of cookies, each with a different deliciously doughy center, just like you can use the same template to make lots of different yummy scrummy webpages.
  Nom nom nom!
</p>